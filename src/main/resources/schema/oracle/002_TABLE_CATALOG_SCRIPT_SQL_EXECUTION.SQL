ALTER TABLE CATALOG_SCRIPT_SQL_EXECUTION ADD CONSTRAINT FK_CATALOG_SQL_EXECUTION FOREIGN KEY (CATALOG_ID) REFERENCES CATALOG_SCRIPT_SQL(CATALOG_ID) ON DELETE CASCADE;
