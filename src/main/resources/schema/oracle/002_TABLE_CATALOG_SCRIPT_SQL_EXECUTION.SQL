CREATE TABLE CATALOG_SCRIPT_SQL_EXECUTION (
CATALOG_ID      VARCHAR2(20 CHAR) NOT NULL,
EXECUTION_DATE  DATE NOT NULL,
MILISECONDS     NUMBER NOT NULL,
STATUS          VARCHAR2(10 CHAR) NOT NULL CHECK (STATUS IN ('SUCCESS','ERROR')),
